#include <iostream>
#include <stdexcept>
#include <algorithm>
#include <cstdint>

#pragma once

class Range {
private:
  int64_t Start;
  int64_t Stop;
  int64_t Step;

public:
  class Iterator {
  private:
    int64_t Current;
    int64_t Step;
    int64_t End;

  public:
    Iterator(int64_t start, int64_t step, int64_t end)
        : Current(start), Step(step), End(end) {}

    int64_t operator*() const { return Current; }

    Iterator& operator++() {
      if ((Step > 0 && Current + Step >= End) || (Step < 0 && Current + Step <= End)) {
        Current = End;
      } else {
        Current += Step;
      }
      return *this;

    }

    Iterator operator++(int) {
      Iterator temp = *this;
      ++(*this);
      return temp;
    }

    bool operator!=(const Iterator& other) const {
      return Current != other.Current;
    }

    bool operator==(const Iterator& other) const {
      return Current == other.Current;
    }
  };

  Range(int64_t end) : Start(0), Stop(end), Step(1)  { }
  Range(int64_t start, int64_t end) : Start(start), Stop(end), Step(1)  { }
  Range(int64_t start, int64_t end, int64_t step) : Start(start), Stop(end), Step(step) { }

  int64_t Size() const {
    return (Step > 0) ? (Stop - Start + Step - 1) / Step
                      : (Start - Stop + (-Step) - 1) / (-Step);
  }

  Iterator begin() const { return Iterator(Start, Step, Stop); }
  Iterator end() const { return Iterator(Stop, Step, Stop); }


};
